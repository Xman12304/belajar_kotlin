fun main(args: Array<String>) {
    val number1 = 12;
    val number2 = 5;
    var result: Int

    result = number1 + number2
    println("$number1 + $number2 = $result");

    result = number1 - number2
    println("$number1 - $number2 = $result");

    result = number1 * number2
    println("$number1 * $number2 = $result");

    result = number1 / number2
    println("$number1 / $number2 = $result");

    result = number1 % number2
    println("$number1 % $number2 = $result");

}